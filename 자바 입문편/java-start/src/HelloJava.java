public class HelloJava { //HelloJava 클래스의 범위 시작
    public static void main(String[] args){ //main() 메서드 범위 시작
        System.out.println("hello java"); //값을 콘솔에 출력함. 문자열을 사용할 땐 쌍따옴표 사용. 세미콜론으로 문장을 구분함.(문장 끝에 세미콜론 필수.
    } //main() 메서드의 범위 끝
} //HelloJava 클래스이 범위 끝


// 자바 언어는 대소문자를 구분함.
// 파일명과 클래스명이 같아야 함.
// 클래스 : 중괄호로 블록을 표시함.
// 자바는 main을 찾아 프로그램을 시작함.(main == 프로그램이 시작점)
// 들여쓰기 없이 코드 실행해도 실행되는데, 가독성을 위해서 들여쓰기를 사용하자
