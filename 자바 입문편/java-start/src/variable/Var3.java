package variable;

public class Var3 {
    public static void main(String[] args) {
        int a;
        a=10; // 변수 초기화
        System.out.println(a);
        a=50; // 변수 변경 (10 -> 50)
        System.out.println(a);
    }
}

/*
    변수를 선언하면 컴퓨터의 공간을 확보해서 그 곳에 데이터를 저장할 수 있음.
    변수의 이름을 통해 해당 메모리 공간에 접근함.

 */